#!/bin/bash
#
# Extract version string from the JSON file.
#
## Usage: version.sh FILE
## 

set -euo pipefail

usage() {
  [ "$*" ] && echo "$0: $*"
  sed -n '/^##/,/^$/s/^## \{0,1\}//p' "$0"
  exit 2
} 2>/dev/null

FILE=$1

VERSION=$(cat ${FILE} \
  | grep version \
  | head -1 \
  | awk -F: '{ print $2 }' \
  | sed 's/[",]//g' \
  | tr -d '[[:space:]]')

echo ${VERSION}
